import Foundation

struct Calculator {

    static func sum(_ first: Double, with second: Double) -> Double {
        return first + second
    }

    static func subtract(_ second: Double, from first: Double) -> Double {
        return first - second
    }

    static func subtract(first: Double, second: Double) -> Double {
        return first - second
    }
}
